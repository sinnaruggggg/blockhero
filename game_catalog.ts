export const STAGE_DEFINITIONS: StageDefinition[] = WORLD_BLUEPRINT.flatMap(
  (world, worldIndex) =>
    CHAPTER_BLUEPRINT.flatMap(chapter =>
      Array.from({length: 10}, (_, index) => {
        const stageNumberInChapter = index + 1;
        const stageNumberInWorld = (chapter.chapterIndex - 1) * 10 + stageNumberInChapter;
        const stageNumberGlobal = (world.id - 1) * 30 + stageNumberInWorld;
        const monsterType = getMonsterType(chapter.chapterIndex, stageNumberInChapter);
        const monster = getMonsterName(worldIndex, monsterType, stageNumberInChapter);
        const baseHp =
          800 +
          world.id * 450 +
          chapter.chapterIndex * 350 +
          stageNumberInChapter * 140;
        const bossHpMultiplier = monsterType === 'boss' ? 7 : monsterType === 'elite' ? 2.5 : 1;

        return {
          id: stageNumberGlobal,
          worldId: world.id,
          worldCode: world.code,
          chapterIndex: chapter.chapterIndex,
          chapterCode: `${world.code}_${chapter.codeSuffix}`,
          stageNumberInChapter,
          stageNumberInWorld,
          stageNumberGlobal,
          name: `${world.name} ${chapter.nameSuffix} ${stageNumberInChapter}`,
          monsterId: monster.monsterId,
          monsterName: monster.monsterName,
          monsterType,
          monsterHp: Math.round(baseHp * bossHpMultiplier),
          monsterAttack: Math.round(
            12 +
              world.id * 5 +
              chapter.chapterIndex * 4 +
              stageNumberInChapter * (monsterType === 'boss' ? 2 : 1),
          ),
          attackIntervalSec: monsterType === 'boss' ? 6 : monsterType === 'elite' ? 8 : 10,
          rewardGold: Math.round(
            120 +
              world.id * 25 +
              chapter.chapterIndex * 20 +
              stageNumberInChapter * 10 +
              (monsterType === 'boss' ? 200 : monsterType === 'elite' ? 80 : 0),
          ),
          rewardCharacterExp: Math.round(
            30 +
              world.id * 5 +
              stageNumberInWorld +
              (monsterType === 'boss' ? 30 : monsterType === 'elite' ? 12 : 0),
          ),
          unlocksBossRaidStage:
            chapter.chapterIndex === 3 && stageNumberInChapter === 10 ? world.id : undefined,
        };
      }),
    ),
);

export const NORMAL_RAID_BOSSES: NormalRaidBossDefinition[] = WORLD_DEFINITIONS.map(
  world => {
    const reward = NORMAL_RAID_DIAMOND_REWARDS.find(entry => entry.stage === world.id);

    if (!reward) {
      throw new Error(`Missing normal raid reward data for stage ${world.id}`);
    }

    return {
      id: `normal_raid_${world.bossId}`,
      stage: world.id,
      name: world.bossName,
      skinId: `skin_${world.bossId}`,
      firstClearDiamondReward: reward.firstClearDiamondReward,
      repeatDiamondReward: reward.repeatDiamondReward,
    };
  },
);

