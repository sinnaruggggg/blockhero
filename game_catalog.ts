function titleize(value: string): string {
  return value
    .split('_')
    .map(part => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function getMonsterType(
  chapterIndex: number,
  stageNumberInChapter: number,
): 'normal' | 'elite' | 'boss' {
  if (chapterIndex === 3 && stageNumberInChapter === 10) {
    return 'boss';
  }

  if (stageNumberInChapter === 5 || stageNumberInChapter === 9) {
    return 'elite';
  }

  return 'normal';
}

function getMonsterName(
  worldIndex: number,
  monsterType: 'normal' | 'elite' | 'boss',
  stageNumberInChapter: number,
): {monsterId: string; monsterName: string} {
  const world = WORLD_BLUEPRINT[worldIndex];

  if (monsterType === 'boss') {
    return {
      monsterId: world.bossId,
      monsterName: world.bossName,
    };
  }

  if (monsterType === 'elite') {
    return {
      monsterId: world.eliteMonster,
      monsterName: titleize(world.eliteMonster),
    };
  }

  const normalMonsterId =
    world.normalMonsters[(stageNumberInChapter - 1) % world.normalMonsters.length];

  return {
    monsterId: normalMonsterId,
    monsterName: titleize(normalMonsterId),
  };
}

