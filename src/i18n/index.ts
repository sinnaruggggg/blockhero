export function t(key: string, ...args: Array<string | number>): string {
  let text = translations[key] ?? key;
  args.forEach((arg, index) => {
    text = text.replace(`{${index}}`, String(arg));
  });
  return text;
}

export function getLang(): Lang {
  return currentLang;
}
